
//Note:- This class contains color code of specific colors

import UIKit


extension UIColor{
    
    static func appThemeColor() -> UIColor {
        return UIColor(red: 253.0 / 255.0, green: 182.0 / 255.0, blue: 76.0 / 255.0, alpha: 1.0)
      //  return UIColor(red: 0/255.0, green: 172/255.0, blue: 0/255.0, alpha: 1.0)
    }

    static func appDarkThemeColor() -> UIColor {
        return UIColor(red: 0/255.0, green: 0/255.0, blue: 0/255.0, alpha: 1.0)
    }
    
    static func appBackgroundColor() -> UIColor {
        return UIColor(red: 242/255.0, green: 242/255.0, blue: 244/255.0, alpha: 1.0)
    }
    
    static func appPlaceholderColor() -> UIColor {
        return UIColor(red: 172/255.0, green: 171/255.0, blue: 177/255.0, alpha: 1.0)
    }
    static func appDocumentPlaceholderColor() -> UIColor {
        return UIColor(red: 255/255.0, green: 179.02/255.0, blue: 179.02/255.0, alpha: 0.76)
    }
    
    static func appTextDarkColor() -> UIColor {
        return UIColor(red: 0/255.0, green: 0/255.0, blue: 0/255.0, alpha: 1.0)
    }
    
    static func appTextLightColor() -> UIColor {
        return UIColor(red: 255/255.0, green: 255/255.0, blue: 255/255.0, alpha: 1.0)
    }
    
    static func appTextMidLightColor() -> UIColor {
        return UIColor(red: 148/255.0, green: 147/255.0, blue: 156/255.0, alpha: 1.0)

    }
    

    
}
